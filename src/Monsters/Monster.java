package Monsters;



// gia tora vgazo to abstract
public class Monster {
    
    protected int hp;    
    protected int dmg;
    protected int xp;
    

    public int getHp(){
        return this.hp;
    }

    public int getDmg(){
        return this.dmg;
    }

    public int getXp(){
        return this.xp;
    }

}

